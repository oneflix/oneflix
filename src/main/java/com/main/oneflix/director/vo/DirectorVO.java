package com.main.oneflix.director.vo;

public class DirectorVO {

	private int rnum;
	private Integer directorId;
	private String directorName;
	private String directorBirth;
	private String searchDirector;

	public int getRnum() {
		return rnum;
	}

	public void setRnum(int rnum) {
		this.rnum = rnum;
	}

	public Integer getDirectorId() {
		return directorId;
	}

	public void setDirectorId(Integer directorId) {
		this.directorId = directorId;
	}

	public String getDirectorName() {
		return directorName;
	}

	public void setDirectorName(String directorName) {
		this.directorName = directorName;
	}

	public String getDirectorBirth() {
		return directorBirth;
	}

	public void setDirectorBirth(String directorBirth) {
		this.directorBirth = directorBirth;
	}

	public String getSearchDirector() {
		return searchDirector;
	}

	public void setSearchDirector(String searchDirector) {
		this.searchDirector = searchDirector;
	}

}
